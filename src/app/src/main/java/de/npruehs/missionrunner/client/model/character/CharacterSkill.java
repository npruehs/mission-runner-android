package de.npruehs.missionrunner.client.model.character;

public class CharacterSkill {
    private String skill;

    private int count;

    public String getSkill() {
        return skill;
    }

    public void setSkill(String skill) {
        this.skill = skill;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
